MODEL_LABELS = {
    "retfound_oct": "RETFound OCT",
    "mirage_slo": "MIRAGE SLO",
    "mean_prob_oct_slo": "Mean fusion",
    "confidence_weighted_oct_slo": "Confidence fusion",
}

MODEL_COLORS = {
    "retfound_oct": "#2f6f9f",
    "mirage_slo": "#c75d4d",
    "mean_prob_oct_slo": "#6a994e",
    "confidence_weighted_oct_slo": "#8a6fbc",
}

TASK_LABELS = {
    "amd": "AMD",
    "dr": "DR",
    "glaucoma": "Glaucoma",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--metrics-root",
        default="equi-agent/outputs/metrics",
        help="Root containing evaluate_predictions.py output directories.",
    )
    parser.add_argument(
        "--out",
        default="equi-agent/outputs/figures/fairvision_experiment_summary.png",
        help="Output PNG path.",
    )
    parser.add_argument(
        "--csv-out",
        default="equi-agent/outputs/tables/fairvision_experiment_summary.csv",
        help="Summary CSV used for plotting.",
    )
    parser.add_argument(
        "--force-fallback",
        action="store_true",
        help="Ignore discovered aggregate CSVs and plot embedded run summary.",
    )
    return parser.parse_args()


def scenario_from_path(path: Path) -> str:
    parts = set(path.parts)
    name = path.parent.name
    if "thresholded" in name:
        return "thresholded"
    if name in {"exp1_standalone_oct", "exp1_standalone_slo"}:
        return "raw"
    return "other"


def fallback_records() -> list[dict[str, object]]:
    columns = ["scenario", "model_name", "task", "auroc", "f1", "balanced_accuracy", "ece"]
    return [dict(zip(columns, row)) for row in FALLBACK_ROWS]


def load_metrics(metrics_root: Path, force_fallback: bool) -> tuple[list[dict[str, object]], str]:
    if force_fallback:
        return fallback_records(), "embedded"

    paths = sorted(metrics_root.glob("**/*_aggregate.csv"))
    records: list[dict[str, object]] = []
    for path in paths:
        scenario = scenario_from_path(path)
        if scenario == "other":
            continue
        needed = {"model_name", "task", "auroc", "f1", "balanced_accuracy", "ece"}
        with path.open("r", newline="") as f:
            reader = csv.DictReader(f)
            if reader.fieldnames is None or not needed.issubset(reader.fieldnames):
                continue
            for row in reader:
                if row["model_name"] not in MODEL_LABELS:
                    continue
                item = {
                    "scenario": scenario,
                    "model_name": row["model_name"],
                    "task": row["task"],
                }
                for metric in ["auroc", "f1", "balanced_accuracy", "ece"]:
                    try:
                        item[metric] = float(row[metric])
                    except ValueError:
                        item[metric] = float("nan")
                records.append(item)

    if records:
        deduped: dict[tuple[str, str, str], dict[str, object]] = {}
        for record in records:
            key = (
                str(record["scenario"]),
                str(record["model_name"]),
                str(record["task"]),
            )
            deduped[key] = record
        return list(deduped.values()), "csv"

    return fallback_records(), "embedded"


def plot_metric(ax, records: list[dict[str, object]], metric: str, title: str, scenario: str, models: list[str]) -> None:
    import numpy as np

    tasks = ["amd", "dr", "glaucoma"]
    width = 0.18
    x = np.arange(len(tasks))

    for i, model in enumerate(models):
        values = []
        for task in tasks:
            matches = [
                row
                for row in records
                if row["scenario"] == scenario
                and row["model_name"] == model
                and row["task"] == task
            ]
            values.append(float(matches[0][metric]) if matches else float("nan"))
        offset = (i - (len(models) - 1) / 2) * width
        ax.bar(
            x + offset,
            values,
            width=width,
            label=MODEL_LABELS[model],
            color=MODEL_COLORS[model],
            edgecolor="#222222",
            linewidth=0.4,
        )

    ax.set_title(title, fontsize=11, pad=8)
    ax.set_xticks(x)
    ax.set_xticklabels([TASK_LABELS[t] for t in tasks])
    ax.set_ylim(0, 1.0)
    ax.grid(axis="y", color="#dddddd", linewidth=0.7)
    ax.set_axisbelow(True)


def main() -> None:
    args = parse_args()

    import matplotlib.pyplot as plt

    records, source = load_metrics(Path(args.metrics_root), args.force_fallback)
    out = Path(args.out)
    csv_out = Path(args.csv_out)
    out.parent.mkdir(parents=True, exist_ok=True)
    csv_out.parent.mkdir(parents=True, exist_ok=True)
    with csv_out.open("w", newline="") as f:
        fieldnames = ["scenario", "model_name", "task", "auroc", "f1", "balanced_accuracy", "ece"]
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(records)

    raw_models = ["retfound_oct", "mirage_slo"]
    threshold_models = [
        "retfound_oct",
        "mirage_slo",
        "mean_prob_oct_slo",
        "confidence_weighted_oct_slo",
    ]

    plt.rcParams.update(
        {
            "font.size": 10,
            "axes.spines.top": False,
            "axes.spines.right": False,
            "figure.facecolor": "white",
            "axes.facecolor": "white",
        }
    )
    fig, axes = plt.subplots(2, 2, figsize=(12, 8), constrained_layout=True)

    plot_metric(
        axes[0, 0],
        records,
        "auroc",
        "Ranking signal on full test set (AUROC)",
        "raw",
        raw_models,
    )
    plot_metric(
        axes[0, 1],
        records,
        "f1",
        "Default threshold behavior (F1 at 0.5)",
        "raw",
        raw_models,
    )
    plot_metric(
        axes[1, 0],
        records,
        "f1",
        "Validation-thresholded classification (F1)",
        "thresholded",
        threshold_models,
    )
    plot_metric(
        axes[1, 1],
        records,
        "ece",
        "Calibration error after fusion/thresholding (ECE)",
        "thresholded",
        threshold_models,
    )
    axes[1, 1].set_ylim(0, 0.42)

    handles, labels = axes[1, 0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="lower center", ncol=4, frameon=False)
    fig.suptitle(
        f"FairVision experiment summary ({source} metrics)",
        fontsize=14,
        y=1.02,
    )
    fig.savefig(out, dpi=220, bbox_inches="tight")
    print(f"source={source}")
    print(f"rows={len(records)}")
    print(f"wrote={out}")
    print(f"wrote_csv={csv_out}")


if __name__ == "__main__":
    main()
