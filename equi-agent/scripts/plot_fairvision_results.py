"""Plot FairVision standalone and fusion experiment summaries.

The script prefers aggregate CSVs generated by evaluate_predictions.py. If those
files are not available in a local mirror, it falls back to the summary values
from the current FairVision runs so a figure can still be generated locally.
"""

